package conditionalstatements;

public class SwitchCase1 {

	public static void main(String[] args) {
	
		int id= 5;
		
		switch (id) {
		case 1 :
			System.out.println("This is for java1");
			break;
		case 2 :
			System.out.println("This is for java2");
		case 3 :
			System.out.println("This is for java3");
		case 4:
			System.out.println("This is for java4");
		case 5:
			System.out.println("This is for java5");
			
		case 6 :
				System.out.println("This is for java6");
			
			break;

		default:
			System.out.println("This is not for java");
			
			break;
		}
		
		
		
			
			
	}

}

package datatypes;

public class Opertors {

	public static void main(String[] args) {
		//addition, sub, mul, div
		int a= 100;
		int b= 200;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		//modulus
		System.out.println(b%a);
		
		//Increment operator
		System.out.println(++a);
		System.out.println(a++);
		
		
		
	}

}

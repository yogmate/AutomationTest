package javasession;

public class ComparisonOperator {
	
	public static void main(String[] args) {
		
		//= Assignment operator
		//==comparison operator
		
		int number1= 100;
		int number2= 200;
		int number3= 300;
		System.out.println(number1==number2);
		System.out.println(number1!=number3);
		System.out.println(number3==number2);
		System.out.println(number1<number2);
		System.out.println(number1>=number2);
		System.out.println(number3>number2);
	}

}
